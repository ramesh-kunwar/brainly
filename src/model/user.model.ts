import mongoose from "mongoose";

const userModel = new mongoose.Schema({
  username: {
    type: String,
    required: [true, "Username is required"],
  },
  email: {
    type: String,
    required: [true, "Email is required"],
    unique: true,
  },
  password: {
    type: String,
    required: [true, "Email is required"],
  },
});

export default mongoose.model("User", userModel);
